func stringToCoord(s string) (float64, float64) {
	coords := strings.Split(s, ",")

	if len(coords) != 2 {
		message := fmt.Sprintf("coords count must be 2, got: %d", len(coords))
		panic(message)
	}

	fstStr := strings.TrimSpace(coords[0])
	fst, err := strconv.ParseFloat(fstStr, 64)
	if err != nil {
		log.Fatal(fstStr, ":", err, "\n", "src:", s)
	}

	sndStr := strings.TrimSpace(strings.TrimSpace(coords[1]))
	snd, err := strconv.ParseFloat(sndStr, 64)
	if err != nil {
		log.Fatal(sndStr, ":", err, "\n", "src:", s)
	}

	return fst, snd
}

type Coords struct {
	Longitude float64
	Latitude  float64
}

func Lookup(str string) []Coords {
	strcords := coordmap[str]

	if len(strcords) < 1 {
		message := fmt.Sprintf("strcords empty. for %s key", str)
		panic(message)
	}

	return common.Fmap(func(s string) Coords {
		fst, snd := stringToCoord(s)
		return Coords{fst, snd}
	}, strcords)
}
